<h4>Веб-шрифты</h4>
<p>
Применение веб-шрифтов CSS3 существенно повысило возможности оформления текста, доступные веб-дизайнерам, позволяя загружать шрифты из Интернета,
а не только со своего пользовательского компьютера, и отображать их по всей Всемирной сети.<br>
Для достижения такого результата нужно объявить веб-шрифт с помощью свойства @font-face:<br>
@font-face<br>
{<br>
font-family:FontName;<br>
src:url('FontName.otf');<br>
}
</p>
<p>
Функция url требует значение, содержащее путь или URL-адрес шрифта.<br>
На большинстве браузеров можно использовать либо шрифты TrueType (TTF), либо шрифты OpenType (OTF),
но Internet Explorer ограничивает вас применением шрифтов TrueType, преобразованных в шрифты EOT (EOT).<br>
Чтобы сообщить браузеру тип шрифта, можно воспользоваться функцией format, как в следующем примере (для шрифтов OpenType):<br>
@font-face<br>
{<br>
font-family:FontName;<br>
src:url('FontName.otf') format('opentype');<br>
}<br><br>
или в этом примере (для шрифтов TrueType):<br>
@font-face<br>
{<br>
font-family:FontName;<br>
src:url('FontName.ttf') format('truetype');<br>
}
</p>
<p>
Чтобы вы увидели, насколько легко можно использовать один из этих шрифтов, в следующем примере показано, как загрузить шрифт Google
(в данном случае Lobster) в ваш HTML-код для использования в заголовках h1>:
</p>
<p>
head><br>
style><br>
h1 { font-family:'Lobster', arial, serif; }<br>
/style><br>
link href='http://fonts.googleapis.com/css?family=Lobster' rel='stylesheet' type='text/css'><br>
/head><br>
